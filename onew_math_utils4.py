"""
온유 시스템을 위한 수학 유틸리티 모듈입니다.
이 모듈은 숫자 관련 기본적인 수학 연산을 제공하며,
주로 다른 모듈에서 재사용될 수 있는 순수 함수들로 구성됩니다.
"""

def square_number(n):
    """
    입력된 숫자의 제곱을 계산하여 반환합니다.

    이 함수는 순수 함수 원칙을 따르며, 외부 상태에 의존하지 않고
    오직 입력 인수 n에만 기반하여 결과를 생성합니다.

    Args:
        n (int 또는 float): 제곱할 숫자. 정수 또는 부동 소수점 숫자가 될 수 있습니다.

    Returns:
        int 또는 float: n의 제곱 값. 입력 n의 타입에 따라 정수 또는 부동 소수점 숫자가 반환됩니다.

    예시:
        >>> square_number(5)
        25
        >>> square_number(2.5)
        6.25
    """
    # 단일 책임 원칙에 따라 이 함수는 오직 숫자의 제곱을 계산하는 책임만 가집니다.
    # 전역 변수를 사용하지 않으며, 모든 필요한 정보는 인수로 전달됩니다.
    return n * n

